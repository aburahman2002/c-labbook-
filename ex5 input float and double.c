#include<stdio.h>
int main()
{
    float num1;
    double num2;
    printf("enter a float value:");
    scanf("%f",&num1);
    printf("enter a double value :");
    scanf("%lf",&num2);
    printf("The float value is %f",num1);
    printf("The double value you have entered is %lf",num2);
    return 0;
}
